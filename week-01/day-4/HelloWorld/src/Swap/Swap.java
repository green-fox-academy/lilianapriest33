package Swap;

public class Swap {
    public static void main(String[] args) {
        int a = 123;
        int b = 526;

        System.out.println("Before swap: a = " + a);
        System.out.println("Before swap: b = " + b);

        int x = a;
        int y = b;

        System.out.println("After swap: a = " + y);
        System.out.println("After swap: b = " + x);
    }

}
